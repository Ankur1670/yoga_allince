        document.addEventListener('DOMContentLoaded', () => {
            AOS.init(); // Initialize AOS

            // Add click event listener for each question
            document.querySelectorAll('.commitment_section .faq-question').forEach((question) => {
                question.addEventListener('click', function() {
                    const toggleSign = this.querySelector('.toggle-sign');
                    const targetCollapse = document.querySelector(this.getAttribute(
                        'data-bs-target'));

                    // Toggle icon on click
                    if (targetCollapse.classList.contains('show')) {
                        toggleSign.textContent = '+';
                        targetCollapse.classList.remove('show');
                    } else {
                        // Reset all other toggle signs to '+' and hide others
                        document.querySelectorAll(
                            '.commitment_section .faq-question .toggle-sign').forEach(
                            (sign) => {
                                sign.textContent = '+';
                            });
                        toggleSign.textContent = '-';
                        targetCollapse.classList.add('show');
                    }

                    // Refresh AOS to ensure animations are re-applied
                    AOS.refresh();
                });
            });
        });
